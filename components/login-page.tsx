"use client";

import React, { useState, useEffect } from "react";
import { Card, CardContent, CardHeader, CardTitle } from "@/components/ui/card";
import { Input } from "@/components/ui/input";
import { Label } from "@/components/ui/label";
import { Button } from "@/components/ui/button";
import { Loader2, Eye, EyeOff } from "lucide-react";

// --- TYPE DEFINITIONS (No changes to logic) ---
export interface AppUser {
  id: string;
  name: string;
  password?: string;
  role: "user" | "admin";
  pages: string[];
}

// --- SVG ICONS (As per target UI) ---
const UserIcon = (props: React.SVGProps<SVGSVGElement>) => (
  <svg
    xmlns="http://www.w3.org/2000/svg"
    width="16"
    height="16"
    viewBox="0 0 24 24"
    fill="none"
    stroke="currentColor"
    strokeWidth="2"
    strokeLinecap="round"
    strokeLinejoin="round"
    {...props}
  >
    <path d="M19 21v-2a4 4 0 0 0-4-4H9a4 4 0 0 0-4 4v2" />
    <circle cx="12" cy="7" r="4" />
  </svg>
);

const LockIcon = (props: React.SVGProps<SVGSVGElement>) => (
  <svg
    xmlns="http://www.w3.org/2000/svg"
    width="16"
    height="16"
    viewBox="0 0 24 24"
    fill="none"
    stroke="currentColor"
    strokeWidth="2"
    strokeLinecap="round"
    strokeLinejoin="round"
    {...props}
  >
    <rect width="18" height="11" x="3" y="11" rx="2" ry="2" />
    <path d="M7 11V7a5 5 0 0 1 10 0v4" />
  </svg>
);

// --- CONSTANTS (No changes to logic) ---
const PUBLIC_SHEET_ID_LOGIN = "1FQwLNqZMJHttaOQnI6Xna5nghHOgQXyWOUfGLAfsfko";
const LOGIN_SHEET_NAME = "Login";

// --- LoginPage Component (Logic untouched, UI updated) ---
const LoginPage: React.FC<{ onLogin: (user: AppUser) => void }> = ({
  onLogin,
}) => {
  const [userId, setUserId] = useState("");
  const [password, setPassword] = useState("");
  const [error, setError] = useState("");
  const [showpassword, setShowPassword] = useState(false);
  const [users, setUsers] = useState<AppUser[]>([]);
  const [isLoading, setIsLoading] = useState(true);

  const APP_SCRIPT_URL =
    "https://script.google.com/macros/s/AKfycbwJn_U3Js50o2YdBN9DFaErLYXKWEDluUf1JjQJGet7d_TN7-O8ZaRWU3bxnf_nc7jAGw/exec";

  useEffect(() => {
    const fetchUsers = async () => {
      setIsLoading(true);
      setError("");
      try {
        const url = `${APP_SCRIPT_URL}?sheet=${LOGIN_SHEET_NAME}&action=fetch`;
        const response = await fetch(url);
        if (!response.ok) {
          throw new Error(
            `Failed to fetch user data: ${response.status} - ${response.statusText}`
          );
        }
        const jsonData = await response.json();
        if (!jsonData.success || !jsonData.data) {
          throw new Error("User data not in expected format.");
        }

        const rows = jsonData.data;
        // Define all possible pages for admin
        const allPossiblePages = ["dashboard", "request", "approval", "form", "reports", "users"];

        // Mapping for specific page names from the sheet to internal route keys
        const pageNameMapping: { [key: string]: string } = {
          'dashboard': 'dashboard',
          'request': 'request',
          'approval': 'approval',
          'add new transaction': 'form', // Maps "add new transaction" to "form"
          'reports': 'reports',
          'repots': 'reports', // Handle typo from sheet
        };

        const fetchedUsers: AppUser[] = rows
          .slice(1)
          .map((row: any) => {
            const id = row[1]; // Column B (index 1) - Username
            const pass = row[2]; // Column C (index 2) - Password (assumed)
            const role = row[3]; // Column D (index 3) - Role
            const pagesString = row[4]; // Column E (index 4) - Pages

            const userRole = role ? String(role).toLowerCase().trim() : "user";
            let pages: string[] = [];

            if (userRole === "admin") {
              // If admin, show all pages
              pages = allPossiblePages;
            } else if (userRole === "user") {
              // If user, parse the comma-separated pages from Column E
              if (pagesString) {
                pages = pagesString
                  .split(",")
                  .map((p: string) => {
                    const params = p.trim().toLowerCase();
                    // Check if the exact param is in our mapping
                    return pageNameMapping[params];
                  })
                  .filter(Boolean) // Remove undefined/null mappings
                  .filter((v: string, i: number, s: string[]) => s.indexOf(v) === i); // Deduplicate
              }
            }

            return {
              id: id ? String(id).trim() : "",
              name: id ? String(id).trim() : "",
              password: pass ? String(pass).trim() : "",
              role: userRole as "user" | "admin",
              pages: pages,
            };
          })
          .filter((u: AppUser) => u.id);

        setUsers(fetchedUsers);
      } catch (err: any) {
        setError(err.message || "An unknown error occurred.");
      } finally {
        setIsLoading(false);
      }
    };
    fetchUsers();
  }, []);

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    setError("");
    const trimmedUserId = userId.trim();
    const trimmedPassword = password.trim();
    if (!trimmedUserId || !trimmedPassword) {
      setError("Please enter both Username and Password");
      return;
    }
    const foundUser = users.find(
      (user) => user.id === trimmedUserId && user.password === trimmedPassword
    );
    if (foundUser) {
      onLogin(foundUser);
    } else {
      setError("Invalid Username or Password");
    }
  };

  return (
    <div className="min-h-screen w-full bg-gradient-to-br from-purple-100 via-purple-50 to-blue-50 flex flex-col items-center justify-center p-4">
      <div className="w-full max-w-xl">
        <Card className="bg-white/95 backdrop-blur-sm border-slate-200/60 shadow-2xl shadow-purple-300/50 rounded-2xl">
          <CardHeader className="text-center pt-12 pb-10">
            <CardTitle className="text-4xl font-bold bg-gradient-to-r from-blue-500 to-purple-500 bg-clip-text text-transparent">
              Pete System
            </CardTitle>
          </CardHeader>
          <CardContent className="px-12 pb-12 pt-4">
            {isLoading ? (
              <div className="flex justify-center items-center py-10">
                <Loader2 className="h-8 w-8 animate-spin text-purple-400" />
              </div>
            ) : (
              <form onSubmit={handleSubmit} className="space-y-8">
                <div className="space-y-2">
                  <Label
                    htmlFor="userId"
                    className="text-slate-600 font-medium flex items-center gap-2 text-sm"
                  >
                    <UserIcon className="text-purple-500" />
                    Username
                  </Label>
                  <Input
                    id="userId"
                    type="text"
                    placeholder="Enter your username"
                    value={userId}
                    onChange={(e) => setUserId(e.target.value)}
                    className="bg-slate-50/50 border-slate-300 h-12 text-base focus:border-purple-400 focus:ring-purple-400/50"
                    required
                  />
                </div>
                <div className="space-y-2">
                  <Label
                    htmlFor="password"
                    className="text-slate-600 font-medium flex items-center gap-2 text-sm"
                  >
                    <LockIcon className="text-purple-500" />
                    Password
                  </Label>
                  <div className="relative">
                    <Input
                      id="password"
                      type={showpassword ? "text" : "password"}
                      placeholder="Enter your password"
                      value={password}
                      onChange={(e) => setPassword(e.target.value)}
                      className="bg-slate-50/50 border-slate-300 h-12 text-base focus:border-purple-400 focus:ring-purple-400/50 pr-10"
                      required
                    />
                    <Button
                      type="button"
                      variant="ghost"
                      size="sm"
                      onClick={() => setShowPassword(!showpassword)}
                      className="absolute right-0 top-0 h-full px-3 py-2 hover:bg-transparent text-slate-400 hover:text-purple-600"
                    >
                      {showpassword ? <EyeOff className="h-5 w-5" /> : <Eye className="h-5 w-5" />}
                    </Button>
                  </div>
                </div>
                {error && (
                  <p className="text-sm text-red-500 text-center pt-1">
                    {error}
                  </p>
                )}
                <Button
                  type="submit"
                  className="w-full bg-gradient-to-r from-blue-500 to-purple-600 hover:opacity-90 transition-opacity duration-300 text-white font-bold py-3 text-lg h-14 rounded-lg shadow-lg shadow-purple-500/40"
                  disabled={isLoading}
                >
                  {isLoading ? (
                    <Loader2 className="mr-2 h-4 w-4 animate-spin" />
                  ) : (
                    "Login"
                  )}
                </Button>
              </form>
            )}
          </CardContent>
        </Card>
      </div>
      <footer className="fixed bottom-0 w-full py-3 text-center text-sm text-white bg-gradient-to-r from-blue-500 to-purple-600">
        Powered by{" "}
        <a
          href="https://www.botivate.in/"
          target="_blank"
          rel="noopener noreferrer"
          className="font-semibold hover:underline"
        >
          Botivate
        </a>
      </footer>
    </div>
  );
};

export default LoginPage;
